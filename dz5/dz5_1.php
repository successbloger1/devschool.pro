$news=  explode("\n", $news);

function print_all($news) {
    for ($i=0; $i<count($news); $i++) {
        echo $i.'. '.$news[$i].'<br>';
    }
}

function print_some_news ($news, $id) {
    echo $id.'. '.$news[$id].'<br>';
}

if (isset($_GET['id'])){
    $id = filter_input(INPUT_GET, 'id', FILTER_VALIDATE_INT); 
    if (($id > count($news))||($id < 0)||($id === FALSE)) {
        echo 'Новости с ID - "'.$_GET['id'].'" нет. Весь список новостей:<br>';
        print_all($news);
    } else {
        echo 'Новость с ID - "'.$id.'": <br>';
        print_some_news ($news, $id);
    }
} else {
    header("HTTP/1.0 404 Not Found");
}